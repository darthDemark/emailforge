function buildMjml(): string {
  return `<mjml>
  <mj-head>
    <mj-attributes>
      <mj-all font-family="Arial, Helvetica, sans-serif" />
      <mj-text font-size="16px" line-height="1.6" color="#444444" />
    </mj-attributes>
    <mj-style>
      @media (prefers-color-scheme: dark) {
        .card { background-color: #1c1c1c !important; }
      }
    </mj-style>
  </mj-head>
  <mj-body background-color="#f4f4f5">
    <mj-section background-color="#ffffff" border-radius="12px" css-class="card" padding="32px">
      <mj-column>
        <mj-image src="https://via.placeholder.com/160x40?text=LOGO" alt="Company logo" width="160px" />
        <mj-text align="center" font-size="28px" font-weight="700" color="#111111">A headline built with live text</mj-text>
        <mj-text>Hi there, this responsive template was generated by EmailForge and compiles to bulletproof HTML.</mj-text>
        <mj-button background-color="#111111" color="#ffffff" border-radius="8px" inner-padding="14px 28px" href="https://example.com">Get started</mj-button>
      </mj-column>
    </mj-section>
    <mj-section padding="16px">
      <mj-column>
        <mj-text align="center" font-size="12px" color="#777777">
          EmailForge Inc., 123 Sender Street, San Francisco, CA 94107<br />
          <a href="{{unsubscribe_url}}">Unsubscribe</a>
        </mj-text>
      </mj-column>
    </mj-section>
  </mj-body>
</mjml>`;
}

