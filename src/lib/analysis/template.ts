import type { ConvertOutputMode } from "@/lib/types";

/**
 * Deterministic, best-practice email template generator. Used as the
 * graceful fallback for CONVERT when no AI provider is configured, and as a
 * reference structure. The output follows every hard requirement: tables,
 * inline CSS, alt/width/height attributes, role="presentation", MSO
 * conditionals, dark-mode meta, 44px CTA, email-safe fonts, no JS/flex/grid.
 */
export function buildStarterEmail(mode: ConvertOutputMode): string {
  if (mode === "mjml") {
    return buildMjml();
  }
  if (mode === "foundation") {
    return buildFoundation();
  }
  return buildStandard(mode === "sfmc");
}

